    public String getCbxUsuarios() {
        String m="";
        if(cbxUsuarios.getActionCommand().equals("Cliente")){
            m="Cliente"; 
        }else if(cbxUsuarios.getActionCommand().equals("Administrador")){
            m="Administrador"; 
        }else if(cbxUsuarios.getActionCommand().equals("Agente de ventas")){
            m="Agente de ventas"; 
        }
        return m;
    }

