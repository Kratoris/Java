    public void setCbxTipoUsuario(String tipo) {
        String tipos[] = {"Cliente", "Administrador", "Agente de ventas"};
        for (int i = 0; i < tipos.length; i++) {
            if (tipos[i].equals(tipo)) {
                cbxTipoUsuario.setSelectedIndex(i);
            }
        }
    }

